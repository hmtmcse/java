public class Switch {
    public static void main(String[] args) {
        int age = 10;
        switch(age){
            case 18:
                System.out.println("Your Age 18");
                break;
            case 30:
                System.out.println("Your Age 30");
                break;
            default:
                System.out.println("Uff! I have no Idea.");
        }
    }
}
