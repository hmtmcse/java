public class Variables {

    public static void main(String[] args) {
        int count; // Integer Variable Declaration
        float x; // Fraction Number Declaration
        char c; //Character Declaration

        count = 10; // Assign Integer Number in Integer Variable
        x = 10.10f; // Assign Fraction Number in Fraction Point Variable
        c = 'c'; // Assign a character in Character Variable

        System.out.println("Integer = " + count);
        System.out.println("Fraction = " + x);
        System.out.println("Character = " + c);

    }

}
